                                    function loadPosts() {
                                        var url = "https://jsonplaceholder.typicode.com/posts"
                                        var i = 0;
                                        $.getJSON(url)
                                            .done((data) => {
                                                $.each(data, (k, item) => {
                                                    // console.log(item);
                                                    i++;
                                                    var line = "<tr>";
                                                    line += "<td>" + item.id + "</td>";
                                                    line += "<td><b>" + item.title + "</b><br/>";
                                                    line += item.body + "</td>";
                                                    line += "<td> <button onClick='showDetails(" + item.id + ");' > link </button> </td>";
                                                    line += "</tr>";
                                                    $("#tblPosts").append(line);
                                                    if (i == 10) {
                                                        loadPosts().stop();
                                                    };
                                                });
                                                $("#main").show();
                                            })
                                            .fail((xhr, status, error) => {
                                            })
                                    }


